n1 = int(input("Digite um dia da semana: "))

if n1 == 1:
    print("Domingo")
if n1 == 2:
    print("Segunda-feira")
if n1 == 3:
    print("Terça-feira")
if n1 == 4:
    print("Quarta-feira")
if n1 == 5:
    print("Quinta-feira")
if n1 == 6:
    print("Sexta-feira")
if n1 == 7:
     print("Sábado")
elif n1 > 7:
    print("Não representa um dia da semana")